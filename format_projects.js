$(document).ready(function() {

  projects.forEach(function(project) {
      createCard(project);
  });

  function createCard(project){
    if (android % 2 == 0) {
      $("#android").append("<div class=\"row\"></div>");
    }
    android++;
    $("#android" ).children().last().append(
        "<div class=\"col s12 m6\">" +
        "  <div class=\"card horizontal\">" +
        "    <div class=\"card-image card-image-padding\">" +
        "      <img src=\"" + project.screenshot + "\" alt=\"Image\"/>" +
        "    </div>" +
        "    <div class=\"card-stacked\">" +
        "    <div class=\"card-content\">" +
        "      <span class=\"card-title purple-text text-darken-4\">" + project.title + "</span>" +
        "      <p>" + project.description + "</p>" +
        "    </div>" +
        "    <div class=\"card-action\">" +
        "      <a href=\"" +project.github +"\" target=\"_blank\">GitHub</a>" +
        "    </div>" +
        "  </div>" +
	    "  </div>" +
        "</div>");
  };
});

